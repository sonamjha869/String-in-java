package Com.String;

class Substring {
	public static void main (String...args) {
		String s ="sonamankit";
				System.out.println("original string:"+s);
				System.out.println("substring starting from index 6: "+s.substring(6)); //nkit
				System.out.println("substring starting from index 0 to 6:" +s.substring(0,6)); // sonama
		
	}

}
